package SOURCE;

public class FacultyReport {
	private String term;
	private String gradeSchool;
	private String courseCode;
	private String courseName;
	private String professor;
	private String avaialbleDays;
	private String maxLoad;
	private String maxStudents;
	/**
	 * @return the term
	 */
	public String getTerm() {
		return term;
	}
	/**
	 * @param term the term to set
	 */
	public void setTerm(String term) {
		this.term = term;
	}
	/**
	 * @return the gradeSchool
	 */
	public String getGradeSchool() {
		return gradeSchool;
	}
	/**
	 * @param gradeSchool the gradeSchool to set
	 */
	public void setGradeSchool(String gradeSchool) {
		this.gradeSchool = gradeSchool;
	}
	/**
	 * @return the courseCode
	 */
	public String getCourseCode() {
		return courseCode;
	}
	/**
	 * @param courseCode the courseCode to set
	 */
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	/**
	 * @return the courseName
	 */
	public String getCourseName() {
		return courseName;
	}
	/**
	 * @param courseName the courseName to set
	 */
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	/**
	 * @return the professor
	 */
	public String getProfessor() {
		return professor;
	}
	/**
	 * @param professor the professor to set
	 */
	public void setProfessor(String professor) {
		this.professor = professor;
	}
	/**
	 * @return the avaialbleDays
	 */
	public String getAvaialbleDays() {
		return avaialbleDays;
	}
	/**
	 * @param avaialbleDays the avaialbleDays to set
	 */
	public void setAvaialbleDays(String avaialbleDays) {
		this.avaialbleDays = avaialbleDays;
	}
	/**
	 * @return the maxLoad
	 */
	public String getMaxLoad() {
		return maxLoad;
	}
	/**
	 * @param maxLoad the maxLoad to set
	 */
	public void setMaxLoad(String maxLoad) {
		this.maxLoad = maxLoad;
	}
	/**
	 * @return the maxStudents
	 */
	public String getMaxStudents() {
		return maxStudents;
	}
	/**
	 * @param maxStudents the maxStudents to set
	 */
	public void setMaxStudents(String maxStudents) {
		this.maxStudents = maxStudents;
	}
	
	

}
