package SOURCE;

public class StudentReport {
	private String gradeSchool;
	private String noofStudents;
	private  String startDate;
	private String completedSubject;
	private String pendingSubject;
	private String availableSubject;
	private String courseCompletionDate;
	/**
	 * @return the gradeSchool
	 */
	public String getGradeSchool() {
		return gradeSchool;
	}
	/**
	 * @param gradeSchool the gradeSchool to set
	 */
	public void setGradeSchool(String gradeSchool) {
		this.gradeSchool = gradeSchool;
	}
	/**
	 * @return the noofStudents
	 */
	public String getNoofStudents() {
		return noofStudents;
	}
	/**
	 * @param noofStudents the noofStudents to set
	 */
	public void setNoofStudents(String noofStudents) {
		this.noofStudents = noofStudents;
	}
	/**
	 * @return the startDate
	 */
	public String getStartDate() {
		return startDate;
	}
	/**
	 * @param startDate the startDate to set
	 */
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	/**
	 * @return the completedSubject
	 */
	public String getCompletedSubject() {
		return completedSubject;
	}
	/**
	 * @param completedSubject the completedSubject to set
	 */
	public void setCompletedSubject(String completedSubject) {
		this.completedSubject = completedSubject;
	}
	/**
	 * @return the pendingSubject
	 */
	public String getPendingSubject() {
		return pendingSubject;
	}
	/**
	 * @param pendingSubject the pendingSubject to set
	 */
	public void setPendingSubject(String pendingSubject) {
		this.pendingSubject = pendingSubject;
	}
	/**
	 * @return the availableSubject
	 */
	public String getAvailableSubject() {
		return availableSubject;
	}
	/**
	 * @param availableSubject the availableSubject to set
	 */
	public void setAvailableSubject(String availableSubject) {
		this.availableSubject = availableSubject;
	}
	/**
	 * @return the courseCompletionDate
	 */
	public String getCourseCompletionDate() {
		return courseCompletionDate;
	}
	/**
	 * @param courseCompletionDate the courseCompletionDate to set
	 */
	public void setCourseCompletionDate(String courseCompletionDate) {
		this.courseCompletionDate = courseCompletionDate;
	}

}
